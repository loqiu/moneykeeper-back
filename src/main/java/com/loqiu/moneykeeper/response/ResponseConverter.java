package com.loqiu.moneykeeper.response;

import org.rochetec.model.response.PayApiResponse;

import java.util.function.Function;

public class ResponseConverter {
    public static <S, T> MkApiResponse<T> convertApiResponse(PayApiResponse<S> source, Function<S, T> converter) {
        if (source == null) {
            return null;
        }
        return MkApiResponse.<T>builder()
                .code(source.getCode())
                .message(source.getMessage())
                .data(converter.apply(source.getData()))
                .build();
    }

}
